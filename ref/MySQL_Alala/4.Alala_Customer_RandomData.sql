USE Alala;

SET SESSION cte_max_recursion_depth = 3000;

-- ============================================
-- Customer 1000건 입력
-- ============================================
INSERT INTO Customer (custid, name, address, phone)
WITH RECURSIVE seq AS (
    SELECT 1 AS n
    UNION ALL
    SELECT n + 1
    FROM seq
    WHERE n < 1000
)
SELECT
    n AS custid,
    CONCAT(
        ELT(1 + MOD(n * 7, 15),
            '김','이','박','최','정','강','조','윤','장','임','한','오','서','신','권'
        ),
        ELT(1 + MOD(n * 11, 20),
            '민','서','지','수','현','도','준','예','하','유',
            '채','다','시','재','태','선','우','보','주','가'
        ),
        ELT(1 + MOD(n * 13, 20),
            '준','민','수','진','현','우','빈','아','윤','호',
            '린','연','원','혁','솔','별','담','율','경','석'
        )
    ) AS name,
    CONCAT(
        ELT(1 + MOD(n * 17, 6),
            '서울특별시', '부산광역시', '대구광역시',
            '인천광역시', '대전광역시', '광주광역시'
        ),
        ' ',
        ELT(1 + MOD(n * 19, 10),
            '강남구', '서초구', '송파구', '마포구', '영등포구',
            '해운대구', '수성구', '남동구', '유성구', '북구'
        ),
        ' ',
        LPAD(MOD(n * 23, 200) + 1, 3, '0'),
        '번지'
    ) AS address,
    CONCAT(
        '010-',
        LPAD(MOD(n * 37, 10000), 4, '0'),
        '-',
        LPAD(MOD(n * 91 + 123, 10000), 4, '0')
    ) AS phone
FROM seq;
